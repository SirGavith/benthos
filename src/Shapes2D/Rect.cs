namespace raylibTest;

public class Rect
{
    public int X;
    public int Y;
    public int Width;
    public int Height;
    public Rect(int x, int y, int width, int height)
    {
        X = x;
        Y = y;
        Width = width;
        Height = height;
    }
}

public class RectF
{
    public float X;
    public float Y;
    public float Width;
    public float Height;
    public RectF(float x, float y, float width, float height)
    {
        X = x;
        Y = y;
        Width = width;
        Height = height;
    }
}